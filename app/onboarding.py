from __future__ import annotations


def build_startup_message() -> str:
    """
    项目启动时给用户的首条 AI 说明文案。
    这里用固定文案，稳定、可控、不会跑偏。
    """
    return (
        "你好，我是智能医疗问诊助手。\n"
        "我可以帮你做这几件事：\n"
        "1. 先询问并整理你的症状信息；\n"
        "2. 将情况分配到合适专科做初步分析；\n"
        "3. 输出可与医生讨论的用药和护理建议。\n\n"
        "请注意：我不能替代线下面诊和处方。\n"
        "如果出现胸痛加重、呼吸困难、意识不清等急症，请立即就医或呼叫急救。\n\n"
        "你可以先按这个格式告诉我：\n"
        "- 主要症状（例如：咳嗽、发热）\n"
        "- 持续时间（例如：3天）\n"
        "- 严重程度（轻/中/重，或0-10分，例如6分）"
    )
